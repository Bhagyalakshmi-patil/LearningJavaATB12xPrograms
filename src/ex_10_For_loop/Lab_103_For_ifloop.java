package ex_10_For_loop;

public class Lab_103_For_ifloop {
    public static void main(String[] args) {
        for (int shruti = 0; shruti < 18 ; shruti++){
            if(shruti > 15 ){
                System.out.println("Get gift");
            }else{
                System.out.println("No gift");
            }
        }
    }
}
